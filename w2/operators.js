//operators in JS

// Modular operator
var x = 52, y=10;
x = x%y;
x


//exponential operator
var a = 10, b = 2;
a = a**b;
a

//comparision operator
var x = 3 == 3;
x

var x = 3 == "3"; //does not check data type
x


//strict operator "===" operator check data type before comparision
var x = 3 === "3";
x
 

//unary operator
var x="3";// string
x= +x;
x// number
//here by using unary operator data type of x has been changed.



 

